package boletindowhile;

public class Ejer2 {

	public static void main(String[] args) {
		// Declaro la variable num para mostrar los números pares
		int num = 2;
		
		// Do-while para imprimir todos los números pares hasta el 200
		do {
			System.out.println(num);
			num += 2;
		} while (num <= 200);

	}

}
